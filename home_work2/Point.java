package lesson2.home_work2;

public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public Point() {
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return y;
    }
}
